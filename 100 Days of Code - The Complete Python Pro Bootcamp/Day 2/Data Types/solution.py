# Subscripting - Uses the list to get the letter from postion 0
print("Hello"[0])

# String
print("123" + "345")

# Integer = Whole number
print(123 + 345)

# Large Integers - Underscore doesn't do anything. It just helps to make the numbers readable for humans.
print(123_456_789)

# Float = Floating Point Number
print(3.14159)

# Boolean
print(True)
print(False)
